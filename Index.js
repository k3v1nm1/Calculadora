function multiplicar(){
    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;

   document.getElementById("respuesta").innerHTML= num1 * num2;

}

function dividir(){
    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;

   document.getElementById("respuesta").innerHTML= num1 / num2;

}

function restar(){
    let num1 = document.getElementById("num1").value;
    let num2 = document.getElementById("num2").value;

   document.getElementById("respuesta").innerHTML= num1 - num2;

}

